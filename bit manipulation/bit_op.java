public class bit_op{
    public static void main(String args[]){
        int n=3; //011
        int b=5; //101
        System.out.println(n&b);
        System.out.println(n|b);
        System.out.println(n^b);
        System.out.println(n>>1);
        System.out.println(b<<1);
        System.out.println(~(b));
  }
}